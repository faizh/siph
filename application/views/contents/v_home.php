          </div><!-- End Tab 1 Content -->

          <div class="tab-pane fade show" id="tab2">
            <p>Sensor kelembaban tanah (soil moisture sensor) adalah perangkat elektronik yang digunakan untuk mengukur tingkat kelembaban tanah di sekitarnya. Cara kerja sensor kelembaban tanah dapat dijelaskan sebagai berikut:</p>
            <div class="d-flex align-items-center mb-2">
              <i class="bi bi-check2"></i>
              <h4>Prinsip Konduktivitas</h4>
            </div>
            <p>Sensor kelembaban tanah umumnya bekerja berdasarkan prinsip konduktivitas listrik. Ketika tanah lembab, konten airnya tinggi, sehingga tanah menjadi konduktif atau mampu menghantarkan listrik. Sebaliknya, ketika tanah kering, konten airnya rendah, sehingga tanah menjadi kurang konduktif.</p>
            <div class="d-flex align-items-center mb-2">
              <i class="bi bi-check2"></i>
              <h4>Elektroda Sensor</h4>
            </div>
            <p>Sensor kelembaban tanah dilengkapi dengan dua elektroda logam yang terpasang pada ujungnya. Ketika sensor ditanamkan ke dalam tanah, kedua elektroda berada dalam kontak dengan tanah di sekitarnya.</p>
          </div><!-- End Tab 2 Content -->

          <div class="tab-pane fade show" id="tab3">
            <p>Sensor ultrasonik bekerja berdasarkan prinsip gelombang suara ultrasonik untuk mengukur jarak antara sensor dengan objek di sekitarnya. Berikut adalah cara kerja sensor ultrasonik secara umum:</p>
            <div class="d-flex align-items-center mb-2">
              <i class="bi bi-check2"></i>
              <h4>Pembangkitan Gelombang Ultrasonik</h4>
            </div>
            <p>Sensor ultrasonik memiliki elemen piezoelektrik di dalamnya. Ketika sinyal listrik diberikan ke elemen piezoelektrik, itu menyebabkan getaran mekanis pada elemen tersebut. Getaran ini menciptakan gelombang suara ultrasonik dengan frekuensi yang sangat tinggi, di luar rentang pendengaran manusia (biasanya di atas 20 kHz).</p>
            <div class="d-flex align-items-center mb-2">
              <i class="bi bi-check2"></i>
              <h4>Pengiriman Gelombang Ultrasonik</h4>
            </div>
            <p>Setelah gelombang ultrasonik dihasilkan, sensor mengirimkan gelombang ini ke objek yang berada di depannya.</p>
          </div><!-- End Tab 3 Content -->

          <div class="tab-pane fade show" id="tab4">
            <p>Sensor LDR (Light Dependent Resistor) atau juga dikenal sebagai fotoreistor adalah jenis sensor yang berubah resistansinya berdasarkan intensitas cahaya di sekitarnya. Berikut adalah cara kerja sensor LDR:</p>
            <div class="d-flex align-items-center mb-2">
              <i class="bi bi-check2"></i>
              <h4>Bahan Semikonduktor</h4>
            </div>
            <p>LDR terbuat dari bahan semikonduktor yang memiliki karakteristik khusus. Ketika cahaya jatuh pada LDR, foton pada cahaya ini dapat menyebabkan elektron pada bahan semikonduktor terlepas dari ikatan dan menciptakan pasangan elektron-holes (lubang).</p>
            <div class="d-flex align-items-center mb-2">
              <i class="bi bi-check2"></i>
              <h4>Perubahan Resistansi</h4>
            </div>
            <p>Ketika cahaya memengaruhi sensor LDR, jumlah pasangan elektron-holes meningkat, menyebabkan resistansi LDR menurun. Artinya, semakin banyak cahaya yang jatuh pada LDR, semakin rendah resistansinya, dan sebaliknya, jika cahaya berkurang, resistansi meningkat.</p>
          </div><!-- End Tab 3 Content -->

          <div class="tab-pane fade show" id="tab5">
            <p>Selenoid Water Valve serupa dengan prinsip kerja umum dari selenoid valve pada umumnya. Selenoid water valve adalah jenis selenoid valve yang digunakan khusus untuk mengontrol aliran air. Berikut adalah langkah-langkah cara kerja selenoid water valve:</p>
            <div class="d-flex align-items-center mb-2">
              <i class="bi bi-check2"></i>
              <h4>Kumparan Kawat (Selenoid)</h4>
            </div>
            <p>Selenoid Water valve memiliki kumparan kawat yang dihubungkan ke sumber listrik, biasanya tegangan DC (arus searah) atau AC (arus bolak-balik).</p>
            <div class="d-flex align-items-center mb-2">
              <i class="bi bi-check2"></i>
              <h4>Gerakan Katup (Valve)</h4>
            </div>
            <p>Katup pada solenoid water valve adalah sebuah bagian yang bergerak, yang bisa berupa piston atau diafragma.</p>
