            <p>NodeMCU ESP32 adalah sebuah mikrokontroler berbasis modul WiFi dan Bluetooth yang sangat populer untuk proyek IoT (Internet of Things). Cara kerja NodeMCU ESP32 melibatkan beberapa aspek berikut:</p>
            <div class="d-flex align-items-center mb-2">
